
# Variables Suma
print("Dame dos números para sumarlos")
print("")

num1 = int(input("Ingresa el primer número: "))
num2 = int(input("Ingresa el segundo número: "))

suma = num1 + num2

print("La suma de {} más {} es: {}".format(num1, num2, suma))



# Variables Resta
